def get_all_docs() -> list[dict]:
    """Получить полный список документов для парсинга."""
    all_docs = (
        LABOR_DOCS
        + LAND_DOCS
        + TAX_DOCS
        + EDUCATION_DOCS
        + HEALTH_DOCS
        + ECOLOGY_DOCS
        + BUSINESS_DOCS
        + ADMIN_DOCS
        + TRANSPORT_DOCS
    )
    # Удалить дубликаты по doc_id
    seen: set[str] = set()
    unique: list[dict] = []
    for doc in all_docs:
        if doc["doc_id"] not in seen:
            seen.add(doc["doc_id"])
            unique.append(doc)
    return unique
